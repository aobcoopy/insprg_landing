<h2>Aims & Scope</h2>
The International Journal of Management, Business, and Economics (IJMBE) is committed to publishing scholarly empirical and theoretical research articles. The journal encourages new ideas or new perspectives on existing research. The journal covers such areas as:<br><br>

•	Business strategy & policy<br>
•	Organizational behavior<br>
•	Human resource management<br>
•	Organizational theory<br>
•	Entrepreneurship<br>
•	Research Methods
